fun main() {
    val x = 5
    val y = 10

    // the following code compares the variables and returns a boolean value

    println("x = y: ${x == y}")          // compares if x is equal to y
    println("x != y: ${x != y}")         // compares if x is not equal to y
    println("x > y: ${x > y}")           // compares if x is greater than y
    println("x < y: ${x < y}")           // compares if x is less than y
    println("x >= y: ${x >= y}")         // compares if x is greater than y
    println("x <= y: ${x <= y}")         // compares if x is less than y
}